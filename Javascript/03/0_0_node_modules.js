/**
 * Assim como qualquer linguagem, podemos baixar coisas feitas
 * por outros usuários (bibliotecas/libs).
 * No javascript temos os Pacotes NPM
 * 
 * NPM -> Node Package Manager
 * 
 * Através do NPM instalamos as libs para serem utilizadas no nosso projeto,
 * elas são baixadas do site https://npmjs.com
 * 
 * o comando é "npm install <nome do pacote>"
 * 
 * Após a instalação será gerado tres coisas:
 * 
 * Pasta node_modules -> Aonde estará tudo que instalamos
 * Arquivo package.json -> Descritivo do seu projeto, a parte importante é que
 *  é nesse arquivo aonde setamos quais libs serão instaladas para o projeto
 * Arquivo package-lock.json -> Arquivo aonde fica uma descrição de como foram 
 *  instaladas a dependencia. Nesse arquivo não vamos mexer, serve apenas pro
 *  npm entender como estão conectado suas libs.
 */